package org.bear.entity;

public class InstitutionalEntity {
	String stockID;
	double oneDay;
	double twoDays;
	double threeDays;
	double fiveDays;
	double tenDays;
	double thirtyDays;
	public String getStockID() {
		return stockID;
	}
	public void setStockID(String stockID) {
		this.stockID = stockID;
	}
	public double getOneDay() {
		return oneDay;
	}
	public void setOneDay(double oneDay) {
		this.oneDay = oneDay;
	}
	public double getTwoDays() {
		return twoDays;
	}
	public void setTwoDays(double twoDays) {
		this.twoDays = twoDays;
	}
	public double getThreeDays() {
		return threeDays;
	}
	public void setThreeDays(double threeDays) {
		this.threeDays = threeDays;
	}
	public double getFiveDays() {
		return fiveDays;
	}
	public void setFiveDays(double fiveDays) {
		this.fiveDays = fiveDays;
	}
	public double getTenDays() {
		return tenDays;
	}
	public void setTenDays(double tenDays) {
		this.tenDays = tenDays;
	}
	public double getThirtyDays() {
		return thirtyDays;
	}
	public void setThirtyDays(double thirtyDays) {
		this.thirtyDays = thirtyDays;
	}
	
}
